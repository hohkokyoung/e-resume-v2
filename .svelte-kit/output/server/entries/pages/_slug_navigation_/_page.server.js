async function getChapter(navigationSlug, chapterSlug, categorySlug) {
  let navigation = await fetchAPI(navigationSlug, chapterSlug) || navigationsData[navigationSlug];
  const chapterNavigations = navigation.map((chapter2) => ({ slug: chapter2.slug, title: chapter2.title }));
  const chapter = navigation.find((chapter2) => chapter2.slug == chapterSlug) || navigation[0];
  chapter.content["category"] = chapter.content.categories.find((category) => category.slug === categorySlug) || chapter.content.categories[0];
  const chapterCategoryNavigations = chapter.content.categories.map((category) => ({ slug: category.slug, title: category.title }));
  return {
    ...chapter,
    "navigations": chapterNavigations,
    "categoryNavigations": chapterCategoryNavigations
  };
}
